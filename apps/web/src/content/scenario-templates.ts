function normalizeQuery(query: string): string {
  return query
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/[^\p{L}\p{N}\s]+/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
}

/**
 * Ищет первый шаблон, триггеры которого есть в запросе пользователя.
 * Простейший keyword-match: если хотя бы один trigger встречается в
 * нормализованной строке — возвращаем этот шаблон.
 *
 * Шаблоны проверяются в порядке объявления в `scenarioTemplates`, поэтому
 * более специфичные (например, `annulled_recovery`) должны идти до общих,
 * если бы они пересекались.
 */
export function findScenarioByTriggers(query: string): ScenarioTemplate | null {
  if (!query || query.trim().length === 0) return null;

  const normalized = normalizeQuery(query);
  if (normalized.length === 0) return null;

  let best: { template: ScenarioTemplate; hits: number } | null = null;

  for (const template of scenarioTemplates) {
    let hits = 0;
    for (const trigger of template.triggers) {
      const t = normalizeQuery(trigger);
      if (t.length === 0) continue;
      if (normalized.includes(t)) {
        hits += 1;
      }
    }
    if (hits > 0 && (best === null || hits > best.hits)) {
      best = { template, hits };
    }
  }

  return best?.template ?? null;
}

/**
 * Возвращает шаблон по id, если такой существует.
 */
export function getTemplateById(id: string): ScenarioTemplate | undefined {
  return scenarioTemplates.find((t) => t.id === id);
}

/**
 * Подставляет значения в плейсхолдеры `{{slot_name}}`.
 *
 * Правила:
 *  - Если для slot'а передано значение — оно подставляется как есть.
 *  - Если slot объявлен в шаблоне, но значения нет — подставляется «(не указано)».
 *  - Любые не объявленные в `slots` плейсхолдеры (например, технические маркеры
 *    внутри шаблонов типа `{{work_address_hint}}`) получают пустую строку,
 *    чтобы не ломать вывод.
 */
export function fillTemplate(
  template: ScenarioTemplate,
  values: Record<string, string>,
): string {
  const declaredSlots = new Set(template.slots.map((s) => s.name));

  return template.template.replace(/\{\{([a-zA-Z0-9_]+)\}\}/g, (_match, key) => {
    const raw = values[key];
    if (typeof raw === "string" && raw.trim().length > 0) {
      return raw;
    }
    if (declaredSlots.has(key)) {
      return "(не указано)";
    }
    return "";
  });
}
