/**
 * Шаблоны структурированных ответов для типовых сложных сценариев.
 *
 * AI-ассистент использует эти шаблоны через tool `getScenarioTemplate`,
 * когда вопрос клиента очевидно матчит один из известных сценариев
 * (лизинг, обжалование штрафа, отказ Дептранса, спецтехника и т.п.).
 *
 * Это даёт более предсказуемые и ровные ответы по критичным темам:
 * меньше галлюцинаций, проще учить, проще дебажить.
 *
 * Архитектура:
 *  - `triggers` — массив ключевых слов для keyword-матчинга поверх
 *    входящего запроса (используется в `findScenarioByTriggers`).
 *  - `slots` — переменные, которые LLM должен заполнить из контекста
 *    разговора (например, гос-номер ТС, название лизингодателя).
 *  - `template` — markdown-строка с placeholder'ами вида `{{slot_name}}`,
 *    которые `fillTemplate` заменяет на реальные значения.
 */

export type ScenarioSlot = {
  /** Техническое имя slot'а, используется как ключ в template и в fillValues. */
  name: string;
  /** Человеко-читаемое описание — LLM использует его, чтобы понять, что сюда подставить. */
  description: string;
  /** Если true — без этого slot'а шаблон считается неполным. */
  required: boolean;
};

export type ScenarioTemplate = {
  /** Уникальный id сценария (например, 'lease_permit'). */
  id: string;
  /** Короткое название для ассистента/UI. */
  title: string;
  /** Одна-две строки про то, когда этот шаблон уместен. */
  description: string;
  /** Ключевые слова для triggers-матчинга (в lower-case, без пунктуации). */
  triggers: string[];
  /** Определения переменных шаблона. */
  slots: ScenarioSlot[];
  /** Сам текст шаблона с `{{slot}}` placeholder'ами. */
  template: string;
};

