/**
 * Данные по классам транспортных средств для программатик-SEO страниц /propusk-na/[slug].
 *
 * Цены указаны в рублях. priceFrom — минимальная стоимость услуги оформления
 * соответствующего годового пропуска (согласовано с services.ts и knowledge-base.ts).
 *
 * При изменении цен в services.ts нужно синхронизировать priceFrom здесь
 * и блоки priceFrom в hero-секциях шаблона страницы.
 */

export type WeightClass = "under-2.5t" | "2.5-3.5t" | "3.5-12t" | "over-12t";

export interface VehicleType {
  /** URL-слаг для маршрута /propusk-na/[slug]. */
  slug: string;
  /** Короткое имя, как употребляется в заголовках (ГАЗель, КАМАЗ). */
  name: string;
  /** Название в именительном падеже с артиклем (на Газель). */
  nameAccusative: string;
  /** Альтернативные названия и популярные модификации. */
  alsoKnownAs: string[];
  /** Класс транспорта. */
  weightClass: WeightClass;
  /** Текстовое описание массы ("3 500 — 12 000 кг"). */
  weightKg: string;
  /** Типичные виды перевозимого груза. */
  typicalCargo: string[];
  /** Типовые маршруты в Москве. */
  commonRoutes: string[];
  /** Какие пропуска нужны и минимальные цены. */
  permitTypes: {
    mkad: { required: boolean; priceFrom: number };
    ttk: { required: boolean; priceFrom: number };
    sk: { required: boolean; priceFrom: number };
    nightDelivery: { needed: boolean; note?: string };
  };
  /** Минимальная стоимость услуги (берётся максимум из "обязательных" зон). */
  priceFrom: number;
  /** SEO-ключи для meta keywords. */
  keywords: string[];
  /** Специфичные боли этого класса ТС. */
  painPoints: string[];
  /** FAQ под модель. */
  faqs: { q: string; a: string }[];
  /** Короткий hero-подзаголовок. */
  heroSubtitle: string;
  /** Расширенное описание для секции "характеристики". */
  summary: string;
  /** Экологический класс минимум для ТТК/СК. */
  ecoClass: string;
}

