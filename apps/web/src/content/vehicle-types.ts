/** Находит описание по slug. */
export function getVehicleBySlug(slug: string): VehicleType | undefined {
  return vehicleTypes.find((v) => v.slug === slug);
}

/** Возвращает 3 связанные модели (случайные другие, исключая текущую). */
export function getRelatedVehicles(currentSlug: string, limit = 3): VehicleType[] {
  const others = vehicleTypes.filter((v) => v.slug !== currentSlug);
  // Пытаемся найти родственные по весовой категории
  const current = getVehicleBySlug(currentSlug);
  if (current) {
    const sameClass = others.filter((v) => v.weightClass === current.weightClass);
    if (sameClass.length >= limit) {
      return sameClass.slice(0, limit);
    }
    // Смешиваем: сначала тот же класс, потом остальные
    const rest = others.filter((v) => v.weightClass !== current.weightClass);
    return [...sameClass, ...rest].slice(0, limit);
  }
  return others.slice(0, limit);
}
