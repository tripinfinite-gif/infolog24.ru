import { Bot } from "grammy";

import { logger } from "@/lib/logger";

export function createBot(token: string): Bot {
  const bot = new Bot(token);

  bot.command("start", async (ctx) => {
    await ctx.reply(
      "Здравствуйте!\n\n" +
        'Я бот компании "Инфологистик-24".\n' +
        "Помогу с оформлением пропусков в Москву для грузового транспорта.\n\n" +
        "Что я умею:\n" +
        "/price -- Рассчитать стоимость пропуска\n" +
        "/status -- Проверить статус заявки\n" +
        "/documents -- Список документов\n" +
        "/contact -- Связаться с менеджером\n" +
        "/help -- Помощь\n\n" +
        "Или просто задайте вопрос!",
    );
  });

  bot.command("help", async (ctx) => {
    await ctx.reply(
      "Доступные команды:\n\n" +
        "/price -- Рассчитать стоимость пропуска\n" +
        "/status -- Проверить статус заявки\n" +
        "/documents -- Список необходимых документов\n" +
        "/contact -- Связаться с менеджером\n\n" +
        "Вы также можете задать любой вопрос о пропусках в свободной форме.",
    );
  });

  bot.command("price", async (ctx) => {
    await ctx.reply(
      "Стоимость пропусков:\n\n" +
        "МКАД:\n" +
        "  Годовой дневной -- 10 000 руб.\n" +
        "  Годовой ночной -- 8 000 руб.\n" +
        "  Временный (до 5 суток) -- 3 500 руб.\n\n" +
        "ТТК:\n" +
        "  Годовой -- 15 000 руб.\n" +
        "  Временный -- 5 000 руб.\n\n" +
        "Садовое кольцо:\n" +
        "  Годовой -- 20 000 руб.\n\n" +
        "Скидки за объём:\n" +
        "  2-5 машин -- 5%\n" +
        "  6-10 машин -- 10%\n" +
        "  от 11 машин -- 15%\n\n" +
        "Для расчёта стоимости напишите, например:\n" +
        '"Рассчитай пропуск на МКАД для 3 машин"',
    );
  });

  bot.command("status", async (ctx) => {
    await ctx.reply(
      "Введите номер вашей заявки (например, ORD-001).\n\n" +
        "Также вы можете отслеживать статус в личном кабинете: infolog24.ru/dashboard",
    );
  });

  bot.command("documents", async (ctx) => {
    await ctx.reply(
      "Документы для оформления пропуска:\n\n" +
        "Базовый набор:\n" +
        "1. СТС (свидетельство о регистрации ТС) -- копия\n" +
        "2. ПТС (паспорт транспортного средства) -- копия\n" +
        "3. Доверенность на управление ТС (если не собственник)\n" +
        "4. Договор на перевозку или маршрутный лист\n" +
        "5. Выписка ЕГРЮЛ/ЕГРИП\n\n" +
        "Дополнительно для ТТК:\n" +
        "  Диагностическая карта техосмотра\n" +
        "  Сертификат экологического класса\n\n" +
        "Дополнительно для Садового кольца:\n" +
        "  Справка о подключении к РНИС (ГЛОНАСС)\n" +
        "  Сертификат экологического класса\n\n" +
        "Документы принимаем по email, WhatsApp или Telegram.",
    );
  });

  bot.command("contact", async (ctx) => {
    await ctx.reply(
      "Связаться с менеджером:\n\n" +
        "Телефон: +7 (495) XXX-XX-XX\n" +
        "Email: info@infolog24.ru\n" +
        "Сайт: infolog24.ru\n\n" +
        "Рабочее время: Пн-Пт 9:00-20:00, Сб 10:00-17:00",
    );
  });

  bot.on("message:text", async (ctx) => {
    // TODO: Send to AI API and return response
    await ctx.reply(
      "Спасибо за ваш вопрос! Наш менеджер скоро ответит.\n\n" +
        "Для быстрой помощи используйте команды:\n" +
        "/price -- Рассчитать стоимость\n" +
        "/documents -- Список документов\n" +
        "/contact -- Связаться с нами",
    );
  });

  bot.on("message:document", async (ctx) => {
    await ctx.reply(
      "Документ получен!\n\n" +
        "Для загрузки документов в систему используйте личный кабинет: infolog24.ru/dashboard/documents",
    );
  });

  bot.catch((err) => {
    logger.error({ error: err.message }, "Telegram bot error");
  });

  return bot;
}
